export class Airplane {
    x: number
    y: number
    raio: number
    angulo: number
    velocidade: number
    direcao: number

    constructor(x: number, y: number, raio: number, angulo: number, velocidade: number, direcao: number) {
        this.x = x
        this.y = x
        this.raio = raio
        this.angulo = angulo
        this.velocidade = velocidade
        this.direcao = direcao
    }
}
